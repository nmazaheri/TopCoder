    public long count(int[] w) {
        count = 0;
        List<Integer> list = new ArrayList();
        for (int i = 0; i < w.length; i++) {
            list.add(w[i]);
            if (list.size() > 3)
                calcPermutations(list, list.get(list.size() - 1));
        }
        return count;
    }

    private void calcPermutations(List<Integer> list, int sum) {
        for (int i = 0; i < list.size() - 1; i++) {
            for (int j = i + 1; j < list.size() - 1; j++) {
                for (int k = j + 1; k < list.size() - 1; k++) {
                    if (list.get(i) * list.get(j) * list.get(k) == sum)
                        count++;
                }
            }
        }
    }
